import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

TextStyle archivo(Color color, double size, FontWeight fw) {
  return GoogleFonts.archivoBlack(
    color: color,
    fontSize: size,
    fontWeight: fw,
  );
}

TextStyle oxygen(Color color, double size, FontWeight fw) {
  return GoogleFonts.oxygen(
    color: color,
    fontSize: size,
    fontWeight: fw,
  );
}
